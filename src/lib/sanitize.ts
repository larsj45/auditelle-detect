/**
 * Escape user-provided strings for safe interpolation into HTML templates.
 * Used in email templates and anywhere user data enters raw HTML.
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;')
}
